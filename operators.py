# Arithmetic Operators

a = 10
b = 3

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Power:", a ** b)
print("Division:", a / b)
print("Modulus:", a % b)

# Logical Operators
print("AND:", a > 5 and b < 5)
print("OR:", a < 5 or b < 5)
print("NOT:", not(a > b))

# Comparison Operators
print("Greater:", a > b)
print("Equal:", a == b)

# Bitwise Operators
print("AND:", a & b)
print("OR:", a | b)
print("XOR:", a ^ b)
print("Left Shift:", a << 2)
print("Right Shift:", b >> 1)
